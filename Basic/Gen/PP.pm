	my($parnames,$parobjs,$dimobjs) = @_;
	"pdl_thread __thread; ".
	 (join '',map {$parobjs->{$_}->get_incdecls} @$parnames).
	 (join '',map {$_->get_decldim} values %$dimobjs);
}

